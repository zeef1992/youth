package vn.youthmanager.ict.common.component;

/**
 * Contains data from config.xml
 * 
 * @author HiepTL
 *
 */
public class XmlConfig {
	private long lastModified;
    private String linePerBlock;
    private String colPerBlock;
    private String dbAccess;

    public long getLastModified() {
        return lastModified;
    }

    public void setLastModified(long lastModified) {
        this.lastModified = lastModified;
    }

	public String getLinePerBlock() {
		return linePerBlock;
	}

	public void setLinePerBlock(String linePerBlock) {
		this.linePerBlock = linePerBlock;
	}

	public String getColPerBlock() {
		return colPerBlock;
	}

	public void setColPerBlock(String colPerBlock) {
		this.colPerBlock = colPerBlock;
	}

	public String getDbAccess() {
		return dbAccess;
	}

	public void setDbAccess(String dbAccess) {
		this.dbAccess = dbAccess;
	}
}